    /**
     * Title generation: raw response versus the cleaned title.
     *
     * @return array<string, mixed>
     */
    private function case_generate_title() {
        $context = $this->build_context();
        $result  = $this->get_generator()->generate_title_for_context($context, $this->sample_article());

        if (is_wp_error($result)) {
            return array('status' => 'failed', 'error' => $result->get_error_message(), 'summary' => $result->get_error_message());
        }

        $raw = $this->last_ai_response_text();

        if (trim((string) $result) === '') {
            return array(
                'status'   => 'failed',
                'error'    => __('The provider returned an empty title.', 'ai-post-scheduler'),
                'summary'  => __('Empty title returned.', 'ai-post-scheduler'),
                'ai_value' => $raw,
            );
        }

        return array(
            'status'       => 'passed',
            'summary'      => sprintf(
                /* translators: %d: character count */
                __('Title generated (%d characters).', 'ai-post-scheduler'),
                mb_strlen($result)
            ),
            'ai_value'     => $raw,
            'plugin_value' => $result,
        );
    }

    /**
     * Content generation: raw response versus normalized post HTML.
     *
     * @return array<string, mixed>
     */
    private function case_generate_content() {
        $context   = $this->build_context();
        $generator = $this->get_generator();

        // Deliberately not generate_preview(): that runs title and excerpt too,
        // which would duplicate the other cases and triple this one's cost. Drive
        // the content step alone so the row measures exactly what it claims to.
        $builder         = new AIPS_Prompt_Builder();
        $content_prompt  = $builder->build_content_prompt($context);
        $content_context = $builder->build_content_context($context);

        $options = array();

        if ($content_context !== '') {
            $options['context'] = $content_context;
        }

        $raw = $generator->generate_content($content_prompt, $options, 'content');

        if (is_wp_error($raw)) {
            return array('status' => 'failed', 'error' => $raw->get_error_message(), 'summary' => $raw->get_error_message());
        }

        $content = $generator->normalize_generated_content_for_wordpress($raw);

        if (trim($content) === '') {
            return array(
                'status'   => 'failed',
                'error'    => __('The provider returned no usable article body.', 'ai-post-scheduler'),
                'summary'  => __('Empty content returned.', 'ai-post-scheduler'),
                'ai_value' => $raw,
            );
        }

        return array(
            'status'       => 'passed',
            'summary'      => sprintf(
                /* translators: 1: word count, 2: character count */
                __('%1$d words / %2$d characters after normalization.', 'ai-post-scheduler'),
                str_word_count(wp_strip_all_tags($content)),
                mb_strlen($content)
            ),
            'ai_value'     => $raw,
            'plugin_value' => $content,
        );
    }

    /**
     * Excerpt generation against a fixed article.
     *
     * @return array<string, mixed>
     */
    private function case_generate_excerpt() {
        $article = $this->sample_article();
        $excerpt = $this->get_generator()->generate_excerpt(
            __('Testing the AI Post Scheduler Pipeline', 'ai-post-scheduler'),
            $article,
            null,
            $this->sample_topic()
        );

        $raw = $this->last_ai_response_text();

        if (trim((string) $excerpt) === '') {
            return array(
                'status'   => 'failed',
                'error'    => __('The provider returned an empty excerpt.', 'ai-post-scheduler'),
                'summary'  => __('Empty excerpt returned.', 'ai-post-scheduler'),
                'ai_value' => $raw,
            );
        }

        return array(
            'status'       => 'passed',
            'summary'      => sprintf(
                /* translators: 1: word count, 2: character count */
                __('%1$d words / %2$d characters stored.', 'ai-post-scheduler'),
                str_word_count($excerpt),
                mb_strlen($excerpt)
            ),
            'ai_value'     => $raw,
            'plugin_value' => $excerpt,
        );
    }

    /**
     * Structured JSON output, including the text-extraction fallback path.
     *
     * @return array<string, mixed>
     */
    private function case_generate_json() {
        $schema = array(
            'type'       => 'object',
            'properties' => array(
                'topics' => array(
                    'type'  => 'array',
                    'items' => array('type' => 'string'),
                ),
            ),
            'required'   => array('topics'),
        );

        $prompt = __('List exactly three blog post topics about sustainable home gardening. Respond with a single JSON object shaped as {"topics": ["first", "second", "third"]} and nothing else.', 'ai-post-scheduler');

        $result = $this->ai_service->generate_json($prompt, array(
            'json_schema' => $schema,
            'max_tokens'  => 300,
        ));

        $raw = $this->last_ai_response_text();

        if (is_wp_error($result)) {
            return array('status' => 'failed', 'error' => $result->get_error_message(), 'summary' => $result->get_error_message(), 'ai_value' => $raw);
        }

        $topics = (isset($result['topics']) && is_array($result['topics'])) ? $result['topics'] : array();

        if (empty($topics)) {
            return array(
                'status'       => 'failed',
                'error'        => __('JSON parsed but contained no "topics" array.', 'ai-post-scheduler'),
                'summary'      => __('Response did not match the requested schema.', 'ai-post-scheduler'),
                'ai_value'     => $raw,
                'plugin_value' => $result,
            );
        }

        return array(
            'status'       => 'passed',
            'summary'      => sprintf(
                /* translators: %d: number of topics */
                __('Parsed %d topics from structured output.', 'ai-post-scheduler'),
                count($topics)
            ),
            'ai_value'     => $raw,
            'plugin_value' => $result,
        );
    }

    /**
     * Image generation plus media library upload.
     *
     * @return array<string, mixed>
     */
    private function case_generate_featured_image() {
        $prompt = __('A bright flat-lay photograph of a wooden desk with a notebook, a cup of coffee, and a small potted plant.', 'ai-post-scheduler');

        $image_service = new AIPS_Image_Service($this->ai_service);
        $attachment_id = $image_service->generate_and_upload_featured_image($prompt, __('AIPS Stress Test Image', 'ai-post-scheduler'));

        $raw = $this->last_ai_response_text();

        if (is_wp_error($attachment_id)) {
            return array('status' => 'failed', 'error' => $attachment_id->get_error_message(), 'summary' => $attachment_id->get_error_message(), 'ai_value' => $raw);
        }

        update_post_meta($attachment_id, self::TEST_ATTACHMENT_META, 1);

        $url = wp_get_attachment_url($attachment_id);

        return array(
            'status'       => 'passed',
            'summary'      => sprintf(
                /* translators: %d: attachment ID */
                __('Image uploaded as attachment #%d.', 'ai-post-scheduler'),
                $attachment_id
            ),
            'ai_value'     => $raw,
            'plugin_value' => array(
                'attachment_id' => (int) $attachment_id,
                'url'           => $url,
            ),
            'artifacts'    => array(
                'attachment_ids' => array((int) $attachment_id),
                'preview_url'    => $url,
            ),
        );
    }

    /**
     * Full pipeline through to a saved draft post.
     *
     * @return array<string, mixed>
     */
    private function case_save_post() {
        $post_id = $this->generate_and_mark_post();

        if (is_wp_error($post_id)) {
            return array('status' => 'failed', 'error' => $post_id->get_error_message(), 'summary' => $post_id->get_error_message());
        }

        $post = get_post($post_id);

        return array(
            'status'       => 'passed',
            'summary'      => sprintf(
                /* translators: %d: post ID */
                __('Draft post #%d created.', 'ai-post-scheduler'),
                $post_id
            ),
            'ai_value'     => $this->first_ai_response_text(),
            'plugin_value' => array(
                'post_id' => (int) $post_id,
                'title'   => $post ? $post->post_title : '',
                'excerpt' => $post ? $post->post_excerpt : '',
                'status'  => $post ? $post->post_status : '',
                'edit_url' => get_edit_post_link($post_id, 'raw'),
            ),
            'artifacts'    => array(
                'post_ids' => array((int) $post_id),
            ),
        );
    }

    /**
     * Repeat the full pipeline to surface rate limits and circuit-breaker trips.
     *
     * A partial run still reports failure but keeps the posts it did create, so
     * the artifacts stay available for inspection and cleanup.
     *
     * @return array<string, mixed>
     */
    private function case_bulk_posts() {
        $post_ids = array();
        $errors   = array();

        for ($i = 0; $i < self::BULK_POST_COUNT; $i++) {
            $post_id = $this->generate_and_mark_post($i + 1);

            if (is_wp_error($post_id)) {
                $errors[] = sprintf('#%d: %s', $i + 1, $post_id->get_error_message());
                continue;
            }

            $post_ids[] = (int) $post_id;
        }

        $created = count($post_ids);
        $summary = sprintf(
            /* translators: 1: created count, 2: requested count */
            __('%1$d of %2$d posts created.', 'ai-post-scheduler'),
            $created,
            self::BULK_POST_COUNT
        );

        return array(
            'status'       => ($created === self::BULK_POST_COUNT) ? 'passed' : 'failed',
            'summary'      => $summary,
            'error'        => empty($errors) ? null : implode(' | ', $errors),
            'ai_value'     => $this->first_ai_response_text(),
            'plugin_value' => array(
                'created'  => $created,
                'expected' => self::BULK_POST_COUNT,
                'post_ids' => $post_ids,
                'errors'   => $errors,
            ),
            'artifacts'    => array(
                'post_ids' => $post_ids,
            ),
        );
    }

    // -----------------------------------------------------------------------
    // Shared fixtures
