<?php
/**
 * Stress Test Service
 *
 * Runs isolated end-to-end exercises against the configured AI provider so an
 * administrator can verify the whole generation stack — credentials, model
 * selection, prompt assembly, response parsing, and post creation — without
 * waiting for a scheduled run.
 *
 * Each case reports two values side by side:
 *   - ai_value:     what the provider returned, before the plugin touched it.
 *   - plugin_value: what the plugin produced after its own processing.
 *
 * That pairing is the point of the page: a case can pass at the transport level
 * and still be wrong after normalization, and the two columns make that visible.
 *
 * Cases that create posts mark them with the _aips_stress_test meta key so they
 * can be listed and removed afterwards; nothing here writes to a published post.
 *
 * @package AI_Post_Scheduler
 * @since 3.2.0
 */

if (!defined('ABSPATH')) {
    exit;
}

class AIPS_Stress_Test_Service {

    /**
     * Post meta key marking a post created by this page.
     */
    const TEST_POST_META = '_aips_stress_test';

    /**
     * Attachment meta key marking an image created by this page.
     */
    const TEST_ATTACHMENT_META = '_aips_stress_test_attachment';

    /**
     * Number of posts generated by the bulk case.
     */
    const BULK_POST_COUNT = 3;

    /**
     * @var AIPS_AI_Service Dedicated service instance so its call log holds only
     *                      the calls made by the case currently running.
     */
    private $ai_service;

    /**
     * @var AIPS_Logger_Interface
     */
    private $logger;

    /**
     * @var AIPS_Generator|null Lazily built; construction pulls in the whole
     *                          prompt builder graph, which idle cases do not need.
     */
    private $generator = null;

    /**
     * @param AIPS_AI_Service|null       $ai_service Optional service override (tests).
     * @param AIPS_Logger_Interface|null $logger     Optional logger override.
     */
    public function __construct($ai_service = null, $logger = null) {
        $container = AIPS_Container::get_instance();

        $this->logger     = $logger ?: ($container->has(AIPS_Logger_Interface::class) ? $container->make(AIPS_Logger_Interface::class) : new AIPS_Logger());
        $this->ai_service = $ai_service ?: new AIPS_AI_Service();
    }

