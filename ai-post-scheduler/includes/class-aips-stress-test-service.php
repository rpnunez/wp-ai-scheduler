    /**
     * Lazily construct the generator.
     *
     * It is wired to this service's own AI service instance so every provider
     * call the generator makes lands in the call log the results panel reads.
     *
     * @return AIPS_Generator
     */
    private function get_generator() {
        if ($this->generator === null) {
            $this->generator = new AIPS_Generator($this->logger, $this->ai_service);
        }

        return $this->generator;
    }

    /**
     * Topic used by every case, so runs stay comparable.
     *
     * @return string
     */
    private function sample_topic() {
        /**
         * Filters the topic the stress test generates against.
         *
         * @since 3.2.0
         *
         * @param string $topic Default sample topic.
         */
        return (string) apply_filters('aips_stress_test_topic', __('Practical tips for starting a balcony herb garden', 'ai-post-scheduler'));
    }

    /**
     * A synthetic template context so the page works on a site with no templates.
     *
     * @param int $index Optional run index, used to vary bulk-run topics.
     * @return AIPS_Template_Context
     */
    private function build_context($index = 0) {
        $topic = $this->sample_topic();

        if ($index > 0) {
            $topic .= sprintf(' (%d)', $index);
        }

        $template = (object) array(
            'id'                      => 0,
            'name'                    => __('AIPS Stress Test', 'ai-post-scheduler'),
            'prompt_template'         => __('Write a short, practical article of roughly 300 words about {{topic}}. Use two <h2> sections.', 'ai-post-scheduler'),
            'title_prompt'            => __('Write a clear, specific title. No colons, no clickbait.', 'ai-post-scheduler'),
            'image_prompt'            => '',
            'generate_featured_image' => false,
            'post_status'             => 'draft',
            'post_type'               => 'post',
            'post_category'           => '',
            'post_tags'               => '',
            'post_author'             => get_current_user_id(),
            'article_structure_id'    => null,
            'include_sources'         => false,
            'source_group_ids'        => '',
        );

        /**
         * Filters the synthetic template used by the stress test.
         *
         * @since 3.2.0
         *
         * @param object $template Template stand-in.
         * @param string $topic    Topic being generated.
         */
        $template = apply_filters('aips_stress_test_template', $template, $topic);

        return new AIPS_Template_Context($template, null, $topic, 'manual');
    }

    /**
     * Run the full pipeline and tag the resulting post as test data.
     *
     * @param int $index Optional run index for bulk runs.
     * @return int|WP_Error Post ID or error.
     */
    private function generate_and_mark_post($index = 0) {
        $post_id = $this->get_generator()->generate_post($this->build_context($index));

        if (!is_wp_error($post_id)) {
            update_post_meta($post_id, self::TEST_POST_META, 1);
        }

        return $post_id;
    }

    /**
     * Fixed article body used by cases that summarize rather than generate.
     *
     * @return string
     */
    private function sample_article() {
        return '<p>' . __('Balcony herb gardens work best when you start with the light you actually have rather than the herbs you want. South-facing rails carry rosemary and thyme through a full season; a shaded corner will do better with mint, parsley, and chives.', 'ai-post-scheduler') . '</p>'
            . '<h2>' . __('Choosing containers', 'ai-post-scheduler') . '</h2>'
