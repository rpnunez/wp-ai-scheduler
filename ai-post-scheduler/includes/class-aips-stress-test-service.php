    /**
     * Count posts and attachments created by this page.
     *
     * @return array{posts: int, attachments: int}
     */
    public function count_test_data() {
        return array(
            'posts'       => count($this->get_test_post_ids()),
            'attachments' => count($this->get_test_attachment_ids()),
        );
    }

    /**
     * IDs of posts created by this page.
     *
     * @return int[]
     */
    private function get_test_post_ids() {
        return get_posts(array(
            'post_type'        => 'any',
            'post_status'      => 'any',
            'numberposts'      => 200,
            'fields'           => 'ids',
            'meta_key'         => self::TEST_POST_META,
            'suppress_filters' => false,
        ));
    }

    /**
     * IDs of attachments created by this page.
     *
     * @return int[]
     */
    private function get_test_attachment_ids() {
        return get_posts(array(
            'post_type'        => 'attachment',
            'post_status'      => 'any',
            'numberposts'      => 200,
            'fields'           => 'ids',
            'meta_key'         => self::TEST_ATTACHMENT_META,
            'suppress_filters' => false,
        ));
    }

    /**
     * Permanently delete everything this page created.
     *
     * @return array{posts: int, attachments: int}
     */
    public function cleanup_test_data() {
        $deleted_posts = 0;
        $deleted_media = 0;

        foreach ($this->get_test_post_ids() as $post_id) {
            // Bypass the trash: these are disposable fixtures, and leaving them in
            // the trash would keep them counted as existing test data.
            if (wp_delete_post($post_id, true)) {
                $deleted_posts++;
            }
        }

        foreach ($this->get_test_attachment_ids() as $attachment_id) {
            if (wp_delete_attachment($attachment_id, true)) {
                $deleted_media++;
            }
        }

        $this->logger->log('Stress test data cleaned up', 'info', array(
            'posts'       => $deleted_posts,
            'attachments' => $deleted_media,
        ));

        return array('posts' => $deleted_posts, 'attachments' => $deleted_media);
    }

    /**
     * Snapshot of the environment shown above the results.
     *
     * @return array<string, mixed>
     */
    public function get_environment() {
        $config   = AIPS_Config::get_instance();
        $provider = AIPS_AI_Provider_Factory::create();

        return array(
            'provider'        => $provider->get_label(),
            'provider_id'     => $provider->get_id(),
            'available'       => $provider->is_available(),
            'reason'          => $provider->is_available() ? '' : $provider->get_unavailable_reason(),
            'model'           => (string) $config->get_option('aips_ai_model'),
            'native_json'     => $provider->supports_native_json(),
            'conversation'    => $provider->supports_conversation(),
            'conversational'  => (bool) $config->get_option('aips_conversational_generation'),
            'metadata_turn'   => (bool) $config->get_option('aips_conversational_metadata_turn'),
        );
    }
