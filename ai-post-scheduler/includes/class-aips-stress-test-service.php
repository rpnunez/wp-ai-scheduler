                'creates'     => true,
            ),
        );
    }

    /**
     * Whether a case id is known.
     *
     * @param string $case_id Case identifier.
     * @return bool
     */
    public function has_case($case_id) {
        foreach ($this->get_cases() as $case) {
            if ($case['id'] === $case_id) {
                return true;
            }
        }

        return false;
    }

    /**
     * Look up a case definition.
     *
     * @param string $case_id Case identifier.
     * @return array<string, mixed>|null
     */
    private function get_case($case_id) {
        foreach ($this->get_cases() as $case) {
            if ($case['id'] === $case_id) {
                return $case;
            }
        }

        return null;
    }

    // -----------------------------------------------------------------------
    // Runner
    // -----------------------------------------------------------------------

    /**
     * Run a single case and return a normalized result.
     *
     * Never throws: an unexpected exception anywhere in the stack is reported as
     * a failed case so one broken provider cannot take down the whole page.
     *
     * @param string $case_id Case identifier.
     * @return array<string, mixed>
     */
    public function run($case_id) {
        $case = $this->get_case($case_id);

        if ($case === null) {
            return $this->fail($case_id, __('Unknown test case.', 'ai-post-scheduler'), 0.0);
        }

        if (!$this->ai_service->is_available()) {
            return $this->fail($case_id, __('No AI provider is available. Check the provider settings.', 'ai-post-scheduler'), 0.0);
        }

        // Isolate the call log so the expanded view shows only this case's traffic.
        $this->ai_service->clear_call_log();

        $started = microtime(true);

        try {
            switch ($case_id) {
                case 'generate_title':
                    $result = $this->case_generate_title();
                    break;
                case 'generate_content':
                    $result = $this->case_generate_content();
                    break;
                case 'generate_excerpt':
                    $result = $this->case_generate_excerpt();
                    break;
                case 'generate_json':
                    $result = $this->case_generate_json();
                    break;
                case 'generate_featured_image':
                    $result = $this->case_generate_featured_image();
                    break;
                case 'save_post':
                    $result = $this->case_save_post();
                    break;
                case 'bulk_posts':
                    $result = $this->case_bulk_posts();
                    break;
                default:
                    $result = array('status' => 'failed', 'error' => __('Case is not implemented.', 'ai-post-scheduler'));
            }
        } catch (Throwable $e) {
            // Throwable, not Exception: a TypeError deep in a provider adapter
            // should still render as a failed row rather than a fatal page.
            $this->logger->log('Stress test case threw: ' . $e->getMessage(), 'error', array('case' => $case_id));

            $result = array(
                'status' => 'failed',
                'error'  => sprintf(
                    /* translators: 1: exception class, 2: message */
                    __('%1$s: %2$s', 'ai-post-scheduler'),
                    get_class($e),
                    $e->getMessage()
                ),
            );
        }

        $duration_ms = (microtime(true) - $started) * 1000;

        return array_merge(
            array(
                'case'         => $case_id,
                'label'        => $case['label'],
                'description'  => $case['description'],
                'group'        => $case['group'],
                'status'       => 'failed',
                'summary'      => '',
                'ai_value'     => null,
                'plugin_value' => null,
                'error'        => null,
                'artifacts'    => array(),
            ),
            $result,
            array(
                'duration_ms' => (int) round($duration_ms),
                'ai_calls'    => $this->collect_ai_calls(),
            )
        );
    }

    /**
     * Build a failed-result envelope.
     *
     * @param string $case_id     Case identifier.
     * @param string $error       Error message.
     * @param float  $duration_ms Elapsed milliseconds.
     * @return array<string, mixed>
     */
    private function fail($case_id, $error, $duration_ms) {
        $case = $this->get_case($case_id);

        return array(
            'case'         => $case_id,
            'label'        => $case ? $case['label'] : $case_id,
            'description'  => $case ? $case['description'] : '',
            'group'        => $case ? $case['group'] : '',
            'status'       => 'failed',
            'summary'      => $error,
            'ai_value'     => null,
            'plugin_value' => null,
            'error'        => $error,
            'artifacts'    => array(),
            'duration_ms'  => (int) round($duration_ms),
            'ai_calls'     => array(),
        );
    }

    /**
     * Normalize the AI service call log into serializable request/response pairs.
     *
     * Data URIs are truncated: a base64 image can be several megabytes and would
     * otherwise be pushed through the AJAX response into the browser.
     *
     * @return array<int, array<string, mixed>>
     */
    private function collect_ai_calls() {
        $calls = array();

        foreach ($this->ai_service->get_call_log() as $entry) {
            $response = isset($entry['response']) ? $entry['response'] : array();
            $content  = isset($response['content']) ? $response['content'] : null;

            $calls[] = array(
                'type'     => isset($entry['type']) ? $entry['type'] : '',
                'time'     => isset($entry['timestamp']) ? $entry['timestamp'] : '',
                'request'  => array(
                    'prompt'  => isset($entry['request']['prompt']) ? (string) $entry['request']['prompt'] : '',
                    'options' => isset($entry['request']['options']) ? $this->scrub_options($entry['request']['options']) : array(),
                ),
                'response' => array(
                    'success' => !empty($response['success']),
                    'content' => $this->shorten_data_uri($content),
                    'error'   => isset($response['error']) ? $response['error'] : null,
                ),
            );
        }

        return $calls;
    }

    /**
     * Remove credentials and non-serializable values from logged options.
     *
     * @param mixed $options Logged options.
     * @return array<string, mixed>
     */
    private function scrub_options($options) {
        if (!is_array($options)) {
            return array();
        }

        unset($options['api_key']);

        foreach ($options as $key => $value) {
            if (is_object($value)) {
                $options[$key] = '[' . get_class($value) . ']';
            }
        }

        return $options;
    }

    /**
     * Replace the body of a long data URI with a placeholder.
     *
     * @param mixed $value Possibly a data URI.
     * @return mixed
     */
    private function shorten_data_uri($value) {
        if (!is_string($value) || strpos($value, 'data:') !== 0 || strlen($value) <= 120) {
            return $value;
        }

        return substr($value, 0, 100) . sprintf('… [%s truncated]', size_format(strlen($value)));
    }

