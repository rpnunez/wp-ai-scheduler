    /**
     * Text of the most recent successful AI response.
     *
     * @return string|null
     */
    private function last_ai_response_text() {
        $log = $this->ai_service->get_call_log();

        for ($i = count($log) - 1; $i >= 0; $i--) {
            if (!empty($log[$i]['response']['success'])) {
                return $this->shorten_data_uri($log[$i]['response']['content']);
            }
        }

        return null;
    }

    /**
     * Text of the first successful AI response.
     *
     * Multi-call cases make several requests; the first is the one whose raw
     * output the plugin then transformed, so that is what belongs beside the
     * final value.
     *
     * @return string|null
     */
    private function first_ai_response_text() {
        foreach ($this->ai_service->get_call_log() as $entry) {
            if (!empty($entry['response']['success'])) {
                return $this->shorten_data_uri($entry['response']['content']);
            }
        }

        return null;
    }

